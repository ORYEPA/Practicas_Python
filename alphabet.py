import turtle
from turtle import *



frame = turtle.Screen().bgcolor("white")

draw = turtle.Turtle()



draw.color("Red")

draw.width(2)

draw.speed(6)





# printing letter L

draw.penup()

draw.goto(135, 70)

draw.pendown()

draw.right(90)

draw.forward(70)

draw.left(90)

draw.forward(35)


done()
